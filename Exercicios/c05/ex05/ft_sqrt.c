#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	a;

	a = 1;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while (a * a <= nb)
	{
		if (a * a == nb)
		{
			return (a);
		}
		if (a >= 46341)
		{
			return (0);
		}
		a++;
	}	
	return (0);
}

