#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

char	*ft_strupcase(char *str)
{
	int	a;

	a = 0;
	while (str[a] != 0)
	{
		if (str[a] >= 'a' && str[a] <= 'z')
		{
		str[a] -= 32;
		}
		a++;
	}
	return (str);
}
